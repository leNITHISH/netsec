SHIFTS = [1, 1, 2, 2, 2, 2, 2, 2, 1, 2, 2, 2, 2, 2, 2, 1]

def h2b(hex_str, n_bits):
    return list(bin(int(hex_str, 16))[2:].zfill(n_bits))

def b2h(bits):
    return hex(int("".join(bits), 2))[2:].upper().zfill(len(bits)//4)

def permutate(bits, table):
    return [bits[i-1] for i in table]

def shift(bits, n):
    return bits[n:] + bits[:n]

def xor(a, b):
    return [str(int(x) ^ int(y)) for x, y in zip(a, b)]

def b2d(b):
    return int("".join(b), 2)

def d2b(d, size):
    return list(bin(d)[2:].zfill(size))

def generate_keys(key_bits):
    keys = []
    key = permutate(key_bits, PC1)
    l, r = key[:28], key[28:]
    print("\n[KEY SCHEDULE]")
    for i, s in enumerate(SHIFTS):
        l = shift(l, s)
        r = shift(r, s)
        k = permutate(l + r, PC2)
        keys.append(k)
        print(f"Round {i+1} Key: {b2h(k)}")
    print("")
    return keys

def feistel(bits, subkey):
    expanded = permutate(bits, E)
    xored = xor(expanded, subkey)
    res = []
    for i in range(8):
        block = xored[i*6: (i+1)*6]
        row = b2d([block[0], block[5]])
        col = b2d(block[1:5])
        val = S_BOX[i][row][col]
        res.extend(d2b(val, 4))
    return permutate(res, P)

def des_block(bits, keys):
    bits = permutate(bits, IP)
    print(f"IP Result: {b2h(bits)}")
    l, r = bits[:32], bits[32:]
    
    for i in range(16):
        l_prev, r_prev = l, r
        l = r_prev
        r = xor(l_prev, feistel(r_prev, keys[i]))
        print(f"Round {i+1:02} | L: {b2h(l)} | R: {b2h(r)}")
        
    final_bits = permutate(r + l, IP_INV)
    return final_bits

